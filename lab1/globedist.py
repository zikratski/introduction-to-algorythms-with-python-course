import sys, math as m

f1, f2 = (float(sys.argv[1])/180)*m.pi, (float(sys.argv[3])/180)*m.pi
la_1,la_2 = (float(sys.argv[2])/180)*m.pi, (float(sys.argv[4])/180)*m.pi
la_ = abs(la_1-la_2)
r = 6378.137
deltnum = ((m.cos(f2)*m.sin(la_))**2+(m.cos(f1)*m.sin(f2)-m.sin(f1)*m.cos(f2)*m.cos(la_))**2)
deltden = m.sin(f1)*m.sin(f2)+m.cos(f1)*m.cos(f2)*m.cos(la_) 
delt = m.atan2(m.sqrt(deltnum),deltden)

print(int(r*delt))
